// berikut ini merupakan contoh membuka window di javascript :
// Membuka halaman web dalam jendela baru
window.open("https://www.example.com");

// Membuka halaman web dalam jendela baru dengan ukuran khusus
window.open("https://www.example.com", "_blank", "width=800,height=600");

// Membuka halaman web dalam jendela baru dan memberikan nama pada jendela tersebut
window.open("https://www.example.com", "myWindow");

// Membuka halaman web dalam jendela baru dengan fitur-fitur tambahan
window.open("https://www.example.com", "_blank", "toolbar=yes,scrollbars=yes,resizable=yes");
