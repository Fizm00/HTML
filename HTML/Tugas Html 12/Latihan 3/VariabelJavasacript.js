// berikut ini merupakan contoh dari variabel javascript
// Variabel dengan tipe data string:
let nama = "John";
let pesan = 'Halo, apa kabar?';

// Variabel dengan tipe data number:
let umur = 25;
let berat = 70.5;

// Variabel dengan tipe data boolean:
let isMahasiswa = true;
let isMarried = false;

// Variabel dengan tipe data array:
let angka = [1, 2, 3, 4, 5];
let buah = ["apel", "mangga", "pisang"];

// Variabel dengan tipe data objek:
let person = {
    nama: "John",
    umur: 25,
    pekerjaan: "Programmer"
};

let mobil = {
    merk: "Toyota",
    warna: "Hitam",
    tahun: 2021
};

// Variabel dengan tipe data null dan undefined:
let kosong = null;
let tidakTerdefinisi = undefined;


//Variabel dengan tipe data fungsi (function):
let tambah = function(a, b) {
    return a + b;
};

let sapa = function(nama) {
    console.log("Halo, " + nama + "!");
};
