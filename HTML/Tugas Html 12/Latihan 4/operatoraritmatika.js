// berikut ini merupakan operator aritmatika :
// Operator Penjumlahan (+):
let a = 5;
let b = 3;
let hasil = a + b; // hasil = 8

// Operator Pengurangan (-):
let a = 5;
let b = 3;
let hasil = a - b; // hasil = 2

// Operator Perkalian (*):
let a = 5;
let b = 3;
let hasil = a * b; // hasil = 15

// Operator Pembagian (/):
let a = 10;
let b = 2;
let hasil = a / b; // hasil = 5

//operator modulus (%):
let a = 10;
let b = 3;
let hasil = a % b; // hasil = 1

// Operator Increment (++) dan Decrement (--):
let a = 5;
a++; // a = 6 (increment)
let b = 3;
b--; // b = 2 (decrement)

// Operator pangkat (**):
let a = 2;
let b = 3;
let hasil = a ** b; // hasil = 8