// berikut ini merupakan operator assignment :
// Operator penugasan (=):
let x = 5;
let y;
y = x; // y akan memiliki nilai 5 yang sama dengan x

// Operator penugasan tambah (+=):
let x = 5;
let y = 3;
x += y; // x akan menjadi 8 (x = x + y)

// Operator penugasan kurang (-=):
let x = 5;
let y = 3;
x -= y; // x akan menjadi 2 (x = x - y)

// Operator penugasan kali (*=):
let x = 5;
let y = 3;
x *= y; // x akan menjadi 15 (x = x * y)

// Operator penugasan bagi (/=):
let x = 10;
let y = 2;
x /= y; // x akan menjadi 5 (x = x / y)

// Operator penugasan modulus (%=):
let x = 10;
let y = 3;
x %= y; // x akan menjadi 1 (x = x % y)

// Operator penugasan pangkat (**=):
let x = 2;
let y = 3;
x **= y; // x akan menjadi 8 (x = x ** y)

