// berikut ini merupakan contoh dari operator logika :
// Operator Logika AND (&&):
let a = true;
let b = false;
console.log(a && b); // Output: false

// Operator Logika OR (||):
let a = true;
let b = false;
console.log(a || b); // Output: true

// Operator Logika NOT (!):
let a = true;
console.log(!a); // Output: false
