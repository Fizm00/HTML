// berikut ini merupakan contoh dari pengulangan/looping :
// Pengulangan For:
for (let i = 0; i < 5; i++) {
    console.log(i);
}

// Pengulangan dengan while:
let i = 0;
while (i < 5) {
    console.log(i);
    i++;
}

// Pengulangan dengan do-while:
let i = 0;
do {
    console.log(i);
    i++;
} while (i < 5);